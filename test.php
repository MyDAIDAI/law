                <p style="text-align: center;"><strong>中国共产党工作机关条例（试行）</strong></p>
                <p style="text-align: center;"><strong>&nbsp;</strong></p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 《中国共产党工作机关条例（试行）》全文如下。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第一章  总则</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第一条  为了规范党的工作机关的设立和运行，提高党的工作机关履职能力和工作水平，保证党的理论和路线方针政策得到有效贯彻执行，根据《中国共产党章程》，制定本条例。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二条  党的工作机关是党实施政治、思想和组织领导的政治机关，是落实党中央和地方各级党委决策部署，实施党的领导、加强党的建设、推进党的事业的执行机关，主要包括办公厅（室）、职能部门、办事机构和派出机关。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第三条  本条例适用于中央和地方党的工作机关。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党委直属事业单位、设在党的工作机关或者由党的工作机关管理的机关，参照本条例执行，法律法规和中央另有规定的除外。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的纪律检查机关的产生和运行，按照党章和中央有关规定执行。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第四条  党的工作机关开展工作应当遵循以下原则：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （一）坚持加强党的领导，坚决维护党中央权威；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （二）坚持党的政治路线、思想路线、组织路线、群众路线；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （三）坚持贯彻民主集中制，增强党的团结统一和机关工作活力；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （四）坚持各司其职、相互配合，确保党的各项工作协调一致、协同推进；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （五）坚持全面从严治党、依规治党，依照党章党规履行职责；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （六）坚持在宪法法律范围内活动，支持同级国家机关和其他组织依法依章程开展工作。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二章  设立</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第五条  党的工作机关的设立，应当适应加强党的领导和党的建设的需要，遵循精简、统一、效能原则，实行总量控制和限额管理。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 根据工作需要，党的工作机关可以与职责相近的国家机关等合并设立或者合署办公。合并设立或者合署办公仍由党委主管。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 严格控制议事协调机构常设办事机构的设立。议事协调机构负责的事项，可以交由现有工作机关牵头协调或者建立协调配合机制解决的，不另设常设办事机构。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第六条  党中央工作机关的设立、撤销、合并或者变更，由中央机构编制管理部门提出方案，按程序报党中央审批决定。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 地方党委工作机关的设立、撤销、合并或者变更，由同级机构编制管理部门提出方案，按规定程序由本级党委讨论决定后，报上级党委审批。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第七条  党的工作机关的领导机构和决策形式是部（厅、室）务会或者委员会，一般由正职、副职、派驻纪检组组长或者纪工委书记及其他成员组成。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的工作机关的领导职数，根据工作需要和从严控制的原则，严格按照有关规定执行。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的工作机关正职由上级机构领导成员兼任的，可以设常务副职，协助其处理日常工作。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的工作机关不设正职领导助理，一般不设秘书长。确有必要时，经党中央批准，党中央职能部门可以设秘书长。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第八条  党的工作机关根据工作需要和精干效能的原则设置必要的内设机构。内设机构的设立、撤销、合并或者变更，按照规定的权限和程序审批。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第九条  党的工作机关在核定的行政编制内配备机关工作人员。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第三章  职责</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十条  党的工作机关应当职责明确、权责一致，其职责一般依据党章党规确定，具体职责由有关职能配置、内设机构、人员编制规定予以明确。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 应当由党委履行的职责，党委不得将其授予工作机关。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十一条  党的工作机关应当履行以下职责：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （一）坚决贯彻落实党的理论和路线方针政策以及党委决策部署，确保政令畅通；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （二）研究部署职责范围内的工作，按照规定制发党内法规和规范性文件，抓好组织实施和督促落实；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （三）当好党委参谋助手，及时报告有关情况、反映问题、提出意见建议，为党委决策提供服务；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （四）抓好机关党的建设工作，加强对本单位群团工作的领导；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （五）承办党委和上级工作机关交办的有关事项。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十二条  党委办公厅（室）是党委的综合部门，负责推动党委决策部署的落实，按照党委要求协调有关方面开展工作，承担党委运行保障具体事务。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十三条  党委职能部门是负责党委某一方面工作的主管部门，按照规定行使相对独立的管理职能，制定相关政策法规并组织实施，协调指导本系统、本领域工作。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十四条  党委办事机构是协助党委办理某一方面重要事务的机构，一般是指党委为加强跨领域、跨部门重要工作的领导和组织协调而设立的议事协调机构的常设办事机构，承担议事协调机构的综合性服务工作，可以根据有关规定履行特定管理职责。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十五条  党委派出机关是党委为加强对特定领域、行业、系统领导而派出的工作机关，根据有关规定代表党委领导该领域、行业、系统的工作。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十六条  党的工作机关必须牢固树立政治意识、大局意识、核心意识、看齐意识，始终在思想上政治上行动上同党中央保持高度一致。按照全面从严治党要求，加强机关党的建设和队伍思想政治建设，教育引导党员干部坚定理想信念，强化宗旨意识，始终保持对党的事业、对党中央的绝对忠诚，自觉践行&ldquo;三严三实&rdquo;要求，在守纪律、讲规矩方面作出表率。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的工作机关应当加强业务能力建设，开展经常性的学习培训和业务交流，勇于探索实践，善于总结工作规律，不断提高干部队伍专业化水平和履职尽责本领。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第四章  决策与执行</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十七条  党的工作机关必须坚持民主集中制，领导班子实行集体领导和个人分工负责相结合的制度。凡属本机关重大事项，应当按照集体领导、民主集中、个别酝酿、会议决定的原则，由领导班子集体研究决定。领导班子成员应当根据集体决定和分工，勇于担当，敢于负责，切实履行职责。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十八条  党的工作机关应当通过召开部（厅、室）务会会议、委员会会议等形式讨论决定下列重大事项：</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （一）学习贯彻党中央、上级和本级党委的有关决定、指示和工作部署；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （二）研究讨论贯彻执行本机关职责范围内相关方针政策与法律法规的具体措施；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （三）讨论决定本机关重大决策、重要人事任免、重大项目安排、大额资金使用等事项；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （四）审议向党中央或者本级党委以及上级党的工作机关请示报告的重要事项；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （五）研究部署本机关党的建设方面的重要事项；</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; （六）研究讨论其他重要事项。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的工作机关领导班子应当科学决策、民主决策、依法决策。对重大事项的决策，一般应当经过调查研究、征求意见、专业评估、合法合规性审查和集体讨论决定等程序。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第十九条  部（厅、室）务会会议、委员会会议由党的工作机关主要负责同志召集并主持，领导班子成员参加。根据工作需要，会议召集人可以确定有关人员列席会议。会议由专门人员如实记录，对决定事项编发会议纪要，并按照规定存档备查。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十条  党的工作机关应当建立有效的督查、评估和反馈机制，确保领导班子决策落实。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十一条  党的工作机关根据工作需要，可以召开部长（主任、书记）办公会议，组织推进部（厅、室）务会会议、委员会会议决策事项的落实和研究讨论专项工作。部长（主任、书记）办公会议由部长（主任、书记）或者委托领导班子其他成员主持召开，领导班子有关成员和有关内设机构主要负责人等参加。部长（主任、书记）办公会议不得代替部（厅、室）务会会议、委员会会议作出决策。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十二条  党的工作机关领导班子及其成员应当加强思想政治建设，认真学习马克思列宁主义、毛泽东思想，坚持用中国特色社会主义理论体系武装头脑，深入学习贯彻习近平总书记系列重要讲话精神和治国理政新理念新思想新战略，不断增强中国特色社会主义的道路自信、理论自信、制度自信、文化自信。严守党的政治纪律和政治规矩，严肃党内政治生活。严格落实中央关于改进工作作风、密切联系群众的各项规定，坚决反对形式主义、官僚主义、享乐主义和奢靡之风。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的工作机关领导班子应当认真履行全面从严治党主体责任，落实党风廉政建设责任制，模范执行廉洁自律各项规定，坚决维护党的纪律，推动形成风清气正、干事创业的良好环境。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第五章  监督与追责</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十三条  党的工作机关接受党委的全面监督，每年至少向党委作1次全面工作情况报告，遇有重要情况及时请示报告。执行党中央和上级党组织某项重要指示和决定的情况，应当进行专题报告。对党的工作机关作出的不适当决定，本级党委或者上级党的工作机关有权撤销或者变更。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 党的工作机关应当自觉接受党的纪律检查机关及其派驻机构、党委直属机关纪工委以及机关纪委的监督。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十四条  党的工作机关领导班子应当自觉接受党内监督和群众监督。领导班子成员应当如实向党组织报告个人有关事项、述职述廉述德，接受组织监督。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十五条  党委应当定期对所属工作机关履职情况进行检查考核，具体工作由党委组织部门负责，考核结果在一定范围内通报。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十六条  党的工作机关领导班子成员违反本条例有关规定的，根据情节轻重，给予批评教育、责令作出检查、诫勉、通报批评或者调离岗位、责令辞职、免职、降职等处理；应当追究党纪政纪责任的，依照有关规定给予相应处分。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第六章  附则</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十七条  机构编制管理部门应当根据本条例科学编制党的工作机关职能配置、内设机构、人员编制规定，按程序报本级党委审批后，以党委文件或者党委办公厅（室）文件形式发布。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十八条  中央军事委员会可以根据本条例，制定相关规定。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第二十九条  本条例由中央办公厅商中央组织部、中央机构编制委员会办公室解释。</p>
                <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 第三十条  本条例自2017年3月1日起施行。</p><BR>
